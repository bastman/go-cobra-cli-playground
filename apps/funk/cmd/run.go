func runPipeline() {
	fmt.Println("runPipeline")
	var source = []int{1, 2, 3, 4}

	r := funk.Map(source, func(x int) int {
		return x * 2
	}) // []int{2, 4, 6, 8}

	fmt.Println("source (json): " + toJson(source))
	fmt.Println("results (json): " + toJson(r))

}

func toJson(v interface{}) string {
	json, _ := json.Marshal(v)
	out := string(json)
	return out
}

